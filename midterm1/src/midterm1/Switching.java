package midterm1;

public class Switching {
	public static void main(String[] args) {
		final int iLoc = 3;    
		switch (iLoc) {  
		case 1:      
			case iLoc:
				System.out.println("I am OK too."); 
				
				case 2 * iLoc:
					System.out.println("I am not OK."); 
					
					case 4:         System.out.println("It's OK.");     
					default:         System.out.println("You are OK.");   			
		}
		}
	} 
